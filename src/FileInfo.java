public class FileInfo {

	/** ファイル名*/
	private String name;

	/** ファイルパス*/
	private String path;

	public FileInfo() {
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPath() {
		return path;
	}

	public void setPath(String path) {
		this.path = path;
	}
}
